;调用DUMP的主程序
		EXTRN DUMP:FAR
.386
DATA0 SEGMENT USE16
ARR		DW 1111,2222,3333,4444,5555,6666,-7777
;定义一个数组,内容为给寄存器赋值，SP不能轻易更改所以总共7个
DATA0	ENDS
STACK SEGMENT USE16 STACK
		DB 200 DUP(0)
STACK ENDS
CODE SEGMENT USE16
		ASSUME DS:DATA0,CS:CODE,SS:STACK
START:
		MOV AX,DATA0
		MOV DS,AX
		MOVSX EAX,ARR;符号位拓展
		MOVSX EBX,ARR+2
		MOVSX ECX,ARR+4
		MOVSX EDX,ARR+6
		MOVSX ESI,ARR+8
		MOVSX EDI,ARR+10
		MOVSX EBP,ARR+12
		CALL DUMP
		MOV AH,4CH
		INT 21H
CODE ENDS
	END START